# git-didi shell integration for bash/zsh
# Add to your shell rc: source path/to/git-didi.bash
# Or use: eval "$(git-didi shell-integration bash)"

# Core git-didi aliases
alias gddi='git-didi'
alias gdds='git-didi stat'
alias gddsc='git-didi stat --color=always'
alias gddp='git-didi patch'
alias gddpc='git-didi patch --color=always'
alias gddpq='git-didi patch --quiet'
alias gddpqc='git-didi patch --quiet --color=always'
alias gddc='git-didi commits'
alias gddcc='git-didi commits --color=always'
alias gddsw='git-didi swatches'
